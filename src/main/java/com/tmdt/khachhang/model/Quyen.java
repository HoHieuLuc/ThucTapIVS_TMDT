package com.tmdt.khachhang.model;

public class Quyen {
    private String ma_quyen, ten_quyen;
    private int cap_do;

    public String getMa_quyen() {
        return ma_quyen;
    }

    public void setMa_quyen(String ma_quyen) {
        this.ma_quyen = ma_quyen;
    }

    public String getTen_quyen() {
        return ten_quyen;
    }

    public void setTen_quyen(String ten_quyen) {
        this.ten_quyen = ten_quyen;
    }

    public int getCap_do() {
        return cap_do;
    }

    public void setCap_do(int cap_do) {
        this.cap_do = cap_do;
    }

}
